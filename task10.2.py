# Задание-10:
# Вывести символы в верхнем регистре, слева от которых находятся
# два символа в нижнем регистре, а справа - два символа в верхнем регистре.
# Т.е. из строки
# "GAMkgAYEOmHBSQsSUHKvSfbmxULaysmNOGIPHpEMujalpPLNzRWXfwHQqwksrFeipEUlTLec"
# нужно получить список строк: ['AY', 'NOGI', 'P']
# Решить задачу двумя способами: с помощью re и без.
line = 'GAMkgAYEOmHBSQsSUHKvSfbmxULaysmNOGIPHpEMujalpPLNzRWXfwHQqwksrFeipEUlTLec'
symbol1 = list(map(lambda x: chr(x), list(range(65, 91))))
symbol2 = list(map(lambda x: chr(x), list(range(97, 123))))
line_new = list(line)

lst = []
i = len(line_new) - 1

while i >= 0:
    if line_new[i] in symbol2:
        lst.append(line_new[i])
    elif line_new[i] in symbol1 and i <= len(line_new) - 3 and line_new[i + 1] in symbol1 and line_new[i + 2] in symbol1:
        lst.append(line_new[i])
    else:
        lst.append(' ')
    i -= 1
lst.reverse()

i = 0
lst2 = []
registr = True

while i <= len(lst) - 1:
    if lst[i] in symbol2:
        registr = True
    if lst[i] in symbol1 and lst[i - 1] in symbol2 and lst[i - 2] in symbol2:
        lst2.append(lst[i])
        registr = False
    elif lst[i] in symbol1 and registr == False:
        lst2.append(lst[i])
    else:
        lst2.append(' ')
    i += 1
linia = ''.join(lst2).split(' ')

line_str_3 = [i for i in linia if i != '']
print(line_str_3)
